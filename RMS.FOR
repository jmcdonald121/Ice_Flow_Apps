      IMPLICIT REAL*8 (A-H,O-Z)
      WRITE(0,10)
10    FORMAT('1','TYPE IN THE X1 COORDINATE')
      READ(0,*) X1
      WRITE(0,11)
11    FORMAT(' ','TYPE IN THE Y1 COORDINATE')
      READ(0,*) Y1
      WRITE(0,12)
12    FORMAT(' ','TYPE IN THE Z1 COORDINATE')
      READ(0,*) Z1
      WRITE(0,13)
13    FORMAT(' ','TYPE IN THE X2 COORDINATE')
      READ(0,*) X2
      WRITE(0,14)
14    FORMAT(' ','TYPE IN THE Y2 COORDINATE')
      READ(0,*) Y2
      WRITE(0,15)
15    FORMAT(' ','TYPE IN THE Z2 COORDINATE')
      READ(0,*) Z2
      WRITE(0,16)
16    FORMAT(' ','TYPE IN X1, X1Y1, X1Z1')
      READ(0,*) DX1,DX1DY1,DX1DZ1
      WRITE(0,17)
17    FORMAT(' ','TYPE IN X1X2, X1Y2, X1Z2')
      READ(0,*) DX1DX2,DX1DY2,DX1DZ2
      WRITE(0,18)
18    FORMAT(' ','TYPE IN Y1, Y1Z1, Y1X2')
      READ(0,*) DY1,DY1DZ1,DY1DX2
      WRITE(0,19)
19    FORMAT(' ','TYPE IN Y1Y2, Y1Z2')
      READ(0,*) DY1DY2,DY1DZ2
      WRITE(0,20)
20    FORMAT(' ','TYPE IN Z1, Z1X2, Z1Y2, Z1Z2')
      READ(0,*) DZ1,DZ1DX2,DZ1DY2,DZ1DZ2
      WRITE(0,21)
21    FORMAT(' ','TYPE IN X2, X2Y2, X2Z2')
      READ(0,*) DX2,DX2DY2,DX2DZ2
      WRITE(0,22)
22    FORMAT(' ','TYPE IN Y2, Y2Z2, Z2')
      READ(0,*) DY2,DY2DZ2,DZ2
      DELX=X2-X1
      DELY=Y2-Y1
      DELZ=Z2-Z1
      D=DSQRT(DELX**2+DELY**2+DELZ**2)
      A1=DX2-2.D0*DX1DX2+DX1
      A2=DY2-2.D0*DY1DY2+DY1
      A3=DZ2-2.D0*DZ1DZ2+DZ1
      B1=2.D0*DX2DY2+2.D0*DX1DY1-2.D0*DY1DX2-2.D0*DX1DY2
      B2=2.D0*DX2DZ2+2.D0*DX1DZ1-2.D0*DZ1DX2-2.D0*DX1DZ2
      B3=2.D0*DY2DZ2+2.D0*DY1DZ1-2.D0*DZ1DY2-2.D0*DY1DZ2
      C1=DELX**2*A1+DELY**2*A2+DELZ**2*A3
      C2=DELY*DELX*B1+DELZ*DELX*B2+DELZ*DELY*B3
      DD=(C1+C2)/D**2
      WRITE(0,28) D,DD
28    FORMAT(' ','D=',F12.3,5X,'RMS=',F12.3)
      STOP
